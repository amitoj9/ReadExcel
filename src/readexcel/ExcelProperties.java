package readexcel;

public class ExcelProperties {

	private String SRI;
	private double min;
	private double max;
	public String getSRI() {
		return SRI;
	}
	public void setSRI(String sRI) {
		SRI = sRI;
	}
	public double getMin() {
		return min;
	}
	public void setMin(double min) {
		this.min = min;
	}
	public double getMax() {
		return max;
	}
	public void setMax(double max) {
		this.max = max;
	}
	
	
}
